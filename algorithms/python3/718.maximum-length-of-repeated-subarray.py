# @lc code=start
class Solution:

    def findLength(self, nums1: List[int], nums2: List[int]) -> int:
        nums1, nums2 = sorted([nums1, nums2], key=len)
        n, m = len(nums1), len(nums2)
        dp = [0] * (n + 1)
        res = 0
        for i in range(n):
            last = 0
            for j in range(m):
                last, dp[j] = dp[j], (nums1[i] == nums2[j]) * (1 + last)
                res = max(res, dp[j])
        return res



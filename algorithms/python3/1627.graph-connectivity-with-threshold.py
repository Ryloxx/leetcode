# @lc code=start
class Solution:

    def areConnected(self, n: int, threshold: int,
                     queries: List[List[int]]) -> List[bool]:
        if threshold <= 0:
            return [1] * len(queries)
        parent = list([1, x] for x in range(n + 1))
        seen = set()

        def find(x):
            if parent[x][1] == x:
                return parent[x]
            return find(parent[x][1])

        def union(x, y):
            (rankx, px), (ranky, py) = sorted([find(x), find(y)])
            if px == py:
                return
            parent[px][1] = py
            parent[py][0] = max(ranky, rankx + 1)

        for i in range(max(threshold + 1, 1), n + 1):
            if i in seen:
                continue
            for j in range(i, n + 1, i):
                seen.add(j)
                union(i, j)

        return list(find(a)[1] == find(b)[1] for a, b in queries)



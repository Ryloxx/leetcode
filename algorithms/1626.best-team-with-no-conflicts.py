from algo_input import run
from types import MethodType


# @lc code=start
class Solution:

    def bestTeamScore(self, scores: List[int], ages: List[int]) -> int:
        dp = [0] * (max(ages) + 1)

        for score, age in sorted(zip(scores, ages)):
            dp[age] = score + max(dp[:age + 1])
        return max(dp)


